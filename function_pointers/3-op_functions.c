/**
 * op_add - Sum two integers values
 * @a: First value
 * @b: Second value
 *
 * Return: Sum of the two values
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - Get the difference of two integers values
 * @a: First value
 * @b: Second value
 *
 * Return: Difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - Get the product of two integers values
 * @a: First value
 * @b: Second vlaue
 *
 * Return: Product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - Get the division of two integers values
 * @a: First value
 * @b: Second value
 *
 * Return: Division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - Get the remainder of the division of two values
 * @a: First value
 * @b: Second value
 *
 * Return: Remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
